package multiformat;

/**
 * Class representing an number base exception.
 *
 * @author Nils Berlijn
 * @author Tom Broenink
 * @version 1.0
 */
public class NumberBaseException extends Exception {

    /**
     * Throws a number base exception.
     *
     * @param message Message.
     */
    public NumberBaseException(String message) {
        super(message);
    }

}
