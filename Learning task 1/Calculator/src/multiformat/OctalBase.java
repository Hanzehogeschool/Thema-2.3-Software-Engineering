package multiformat;

/**
 * Octal numbering base.
 *
 * @author Nils Berlijn
 * @author Tom Broenink
 * @version 1.0
 */
public class OctalBase extends Base {

    /**
     * Octal base constructor.
     * Parameter-free constructor.
     * Creates a new octal base.
     */
    public OctalBase() {
        super("oct", 8, "01234567");
    }

}
